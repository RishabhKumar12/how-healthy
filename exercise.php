  <p  style="font-size:13px"><b>1.Push ups:</b></p><br />

  <p  style="font-size:13px">This is my all time favourite exercise to do in the house and can be done almost anywhere. If done correctly it can strengthen stamina and build your Pecs and triceps.</p><br />


<p style="font-size:13px"><b>How to do a standard push up:</b></p><br />


<p style="font-size:13px">Lie face down with your palms on the floor, about shoulder width apart. Push your whole body up with your back and legs in a straight line and then lower yourself back down towards the floor. To strengthen your chest more place your arms wider apart. To strengthen your triceps and back more, move your arms closer together. Muscles used: Chest muscles (pectoral) Triceps (backs of the upper arm) As a guide I have shown some stats for pushups from the armed forces.</p><br />


<p style="font-size:13px"><b>2.Sit ups</b></p><br />



<p style="font-size:13px">This is another great exercise to do in the house and aging can be done almost anywhere. There are lots of ways to do sit ups. I am using the standard sit up. How to do a sit up: Lie on your back with your knees bens and feet flat on the floor. Cross your arms across your chest and raise your body up to try and meet your knees. This can cause strain on the back, so if you have a weak back it may be better to do a crunch instead. Muscles used in sit ups: Abdominal muscles Back muscles.</p><br />
   

	
<p style="font-size:13px"><b>3.Squats</b></p><br />


<p style="font-size:13px">Another easy but very useful exercise for your thighs and buttocks. It helps to shape your buttocks, strengthen thighs and build stamina. How to do it: Stand up straight, bend your knees with your arms stretched outright parallel to the floor. Bend until your thighs are parallel to the floor. Muscles used; Thigh muscles Buttock muscles.</p><br />


 
