    <h2> How to Lose Weight Fast</h2>
    <p>If you burn 500 more calories than you eat every day for a week, you should lose about 1-2 pounds.<br />
    </p>
    <p>If you want to lose weight faster, you'll need to eat less and exercise more.<br />
    </p>
    <p>For instance, if you take in 1,050 to 1,200 calories a day, and exercise for one hour per day, you could lose 3-5 pounds in the first week, or more if you weigh more than 250 pounds. It's very important not to cut calories any further -- that's dangerous.<br />
    </p>
    <p>Limiting salt and starches may also mean losing more weight at first -- but that's mostly fluids, not fat.<br />
    </p>
    <p>"When you reduce sodium and cut starches, you reduce fluids and fluid retention, which can result in up to 5 pounds of fluid loss when you get started," says Michael Dansinger, MD, of NBC's The Biggest Loser show.<br />
    </p>
    <h2>Diets for Fast Weight Loss</h2>
    <p>Dansinger recommends eating a diet that minimizes starches, added sugars, and animal fat from meat and dairy foods. For rapid weight loss, he recommends focusing  on fruits, veggies, egg whites, soy products, skinless poultry breasts, fish, shellfish, nonfat dairy foods, and 95% lean meat.<br />
    </p>
    <p>Here are more tips from Dawn Jackson Blatner, RD, author of The Flexitarian Diet:<br />
    </p>
    <p>1.Eat vegetables to help you feel full.<br />
    </p>
    <p>2. Drink plenty of water.<br />
    </p>
    <p>3. Get tempting foods out of your home.<br />
    </p>
    <p>4. Stay busy -- you don't want to eat just because you're bored.<br />
    </p>
    <p>5. Eat only from a plate, while seated at a table. No grazing in front of the 'fridge.<br />
    </p>
    <p>6. Don't skip meals.<br />
    </p>
    <p>Keeping a food journal -- writing down everything you eat -- can also help you stay on track.<br />
    </p>
    <p>"Even if you write it down on a napkin and end up throwing it away, the act of writing it down is about being accountable to yourself and is a very effective tool for weight loss," says Bonnie Taub Dix, MA, RD, author of Read It Before You Eat It.<br />
    </p>
    .
    <p>Besides jotting down what you ate, and when, you might also want to note how you were feeling right before you ate it. Were you angry, sad, or bored? We often focus so much on foods and calories, but our emotions are a huge part of our eating habits.<br />
    </p>
    <p>If you see a persistent pattern in your emotional eating, please consider talking to a counselor about it. They can be a big help in finding other ways to handle your feelings.<br />
    </p>
    <p>
    <h2>Exercising for Fast Weight Loss</h2>
    </p>
    <p>It's time to move more! Losing weight requires close to an hour a day of moderate exercise, one study shows.<br />
    </p>
    <p>Plan to do cardio and strength training.<br />
    </p>
    <p>"Cardio burns the most calories, so it is ideal for fast weight loss, but afterward you need to include a few hours a week of strength training," Dansinger says. To burn the most fat, try to break a sweat after your warm-up and keep sweating for the entire hour, Dansinger says.<br />
    </p>
    <p>If you're not exercising now, and you have a chronic condition or a lot of weight to lose, it's wise to check in with your health care provider first. They'll be rooting for you! And they'll make sure that you're ready to work out.<br />
    </p>
    <p>Pace yourself. Don't do too much, too soon -- work your way up to help prevent injury.<br />
    </p>
    <p>One way to step up the intensity is to do interval training -- brief bursts of high-intensity, followed by a more mellow pace, and repeating that pattern throughout your workout.<br />
    </p>
    <p>"Interval training allows people to work harder without having to spend the entire time at the higher level, and over time, the more you do it, the easier it becomes to burn more calories," Blatner says.
      Fad Diets and Crash Diets<br />
    </p>
    <p>I know how tempting diet crazes can sound, especially if you have a lot of weight to lose. You hear about stars who did it and look incredible.<br />
    </p>
    <p>But remember,  if a diet plan sounds too good to be true, it probably is.<br />
    </p>
    <p>Also, please skip any programs that promote detoxification pills, laxatives, fasting, or potions, and any that promise weight loss faster than 2-3 pounds per week.<br />
    </p>
    <p>The truth is that cutting calories below 1,050-1,200 per day is counterproductive, because you need strong muscles to be able to exercise effectively.<br />
    </p>
    <p>"When you eat too few calories, you lose fat but also precious muscle, which is the worst thing you could do because it slows your metabolism and makes it more difficult to increase exercise intensity or duration," Dansinger says.<br />
    </p>
    <p>Fad diets also set you up for failure by depriving you of what you want. You can't eat like that for long, and it's too likely that you'll rebel and end up back where you started. You deserve better than that!<br />
    </p>
    <p>So by all means, attack your weight loss goal. Put it on the fast track. But please, do it right so you set yourself up for lasting success.<br />
    </p>
